create extension if not exists pgcrypto;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text unique,
  full_name text,
  role text not null default 'student' check (role in ('student', 'faculty', 'admin')),
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected')),
  student_id text unique,
  employee_id text unique,
  department text,
  year_section text,
  program text,
  avatar_url text,
  approved_by uuid references public.profiles(id),
  approved_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.conversations (
  id uuid primary key default gen_random_uuid(),
  title text,
  is_group boolean not null default false,
  created_by uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.conversation_members (
  conversation_id uuid not null references public.conversations(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  joined_at timestamptz not null default now(),
  primary key (conversation_id, user_id)
);

create table if not exists public.messages (
  id uuid primary key default gen_random_uuid(),
  conversation_id uuid not null references public.conversations(id) on delete cascade,
  sender_id uuid not null references public.profiles(id) on delete cascade,
  body text not null,
  created_at timestamptz not null default now(),
  read_at timestamptz
);

create table if not exists public.notifications (
  id uuid primary key default gen_random_uuid(),
  title text not null,
  body text not null,
  type text not null default 'announcement',
  target_role text check (target_role in ('student', 'faculty', 'admin')),
  target_user_id uuid references public.profiles(id) on delete cascade,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now()
);

create table if not exists public.campus_locations (
  id bigint generated by default as identity primary key,
  name text not null,
  category text not null,
  description text,
  building_code text,
  latitude double precision,
  longitude double precision,
  image_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create trigger trg_profiles_updated_at
before update on public.profiles
for each row execute procedure public.set_updated_at();

create trigger trg_conversations_updated_at
before update on public.conversations
for each row execute procedure public.set_updated_at();

create trigger trg_campus_locations_updated_at
before update on public.campus_locations
for each row execute procedure public.set_updated_at();

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  role_value text;
  id_number_value text;
begin
  role_value := coalesce(new.raw_user_meta_data->>'role', 'student');
  if role_value not in ('student', 'faculty', 'admin') then
    role_value := 'student';
  end if;

  id_number_value := nullif(new.raw_user_meta_data->>'id_number', '');

  insert into public.profiles (
    id,
    email,
    full_name,
    role,
    status,
    department,
    year_section,
    program,
    student_id,
    employee_id
  ) values (
    new.id,
    new.email,
    nullif(coalesce(new.raw_user_meta_data->>'full_name', trim(coalesce(new.raw_user_meta_data->>'first_name', '') || ' ' || coalesce(new.raw_user_meta_data->>'last_name', ''))), ''),
    role_value,
    'pending',
    nullif(new.raw_user_meta_data->>'department', ''),
    nullif(new.raw_user_meta_data->>'year_section', ''),
    nullif(new.raw_user_meta_data->>'program', ''),
    case when role_value = 'student' then id_number_value else null end,
    case when role_value in ('faculty', 'admin') then id_number_value else null end
  )
  on conflict (id) do update
  set
    email = excluded.email,
    full_name = coalesce(excluded.full_name, public.profiles.full_name),
    role = excluded.role,
    department = coalesce(excluded.department, public.profiles.department),
    year_section = coalesce(excluded.year_section, public.profiles.year_section),
    program = coalesce(excluded.program, public.profiles.program),
    student_id = coalesce(excluded.student_id, public.profiles.student_id),
    employee_id = coalesce(excluded.employee_id, public.profiles.employee_id),
    updated_at = now();

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute procedure public.handle_new_user();

create or replace function public.is_admin(check_user uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles p
    where p.id = check_user
      and p.role = 'admin'
      and p.status = 'approved'
  );
$$;

