-- PROFILES
drop policy if exists "profiles_select_self_or_admin" on public.profiles;
create policy "profiles_select_self_or_admin"
on public.profiles
for select
using (auth.uid() = id or public.is_admin(auth.uid()));

drop policy if exists "profiles_insert_self" on public.profiles;
create policy "profiles_insert_self"
on public.profiles
for insert
with check (auth.uid() = id);

drop policy if exists "profiles_update_self_or_admin" on public.profiles;
create policy "profiles_update_self_or_admin"
on public.profiles
for update
using (auth.uid() = id or public.is_admin(auth.uid()))
with check (auth.uid() = id or public.is_admin(auth.uid()));

-- CONVERSATIONS
drop policy if exists "conversations_member_read" on public.conversations;
create policy "conversations_member_read"
on public.conversations
for select
using (
  exists (
    select 1
    from public.conversation_members m
    where m.conversation_id = public.conversations.id
      and m.user_id = auth.uid()
  )
);

drop policy if exists "conversations_create_authenticated" on public.conversations;
create policy "conversations_create_authenticated"
on public.conversations
for insert
with check (auth.uid() = created_by);

drop policy if exists "conversations_update_creator_or_admin" on public.conversations;
create policy "conversations_update_creator_or_admin"
on public.conversations
for update
using (auth.uid() = created_by or public.is_admin(auth.uid()))
with check (auth.uid() = created_by or public.is_admin(auth.uid()));

-- CONVERSATION MEMBERS
drop policy if exists "conversation_members_read_member" on public.conversation_members;
create policy "conversation_members_read_member"
on public.conversation_members
for select
using (
  user_id = auth.uid()
  or exists (
    select 1
    from public.conversation_members me
    where me.conversation_id = public.conversation_members.conversation_id
      and me.user_id = auth.uid()
  )
  or public.is_admin(auth.uid())
);

drop policy if exists "conversation_members_insert_creator_or_admin" on public.conversation_members;
create policy "conversation_members_insert_creator_or_admin"
on public.conversation_members
for insert
with check (
  public.is_admin(auth.uid())
  or exists (
    select 1
    from public.conversations c
    where c.id = public.conversation_members.conversation_id
      and c.created_by = auth.uid()
  )
);

-- MESSAGES
drop policy if exists "messages_read_member" on public.messages;
create policy "messages_read_member"
on public.messages
for select
using (
  exists (
    select 1
    from public.conversation_members m
    where m.conversation_id = public.messages.conversation_id
      and m.user_id = auth.uid()
  )
  or public.is_admin(auth.uid())
);

drop policy if exists "messages_insert_member" on public.messages;
create policy "messages_insert_member"
on public.messages
for insert
with check (
  auth.uid() = sender_id
  and exists (
    select 1
    from public.conversation_members m
    where m.conversation_id = public.messages.conversation_id
      and m.user_id = auth.uid()
  )
);

drop policy if exists "messages_update_sender_or_admin" on public.messages;
create policy "messages_update_sender_or_admin"
on public.messages
for update
using (auth.uid() = sender_id or public.is_admin(auth.uid()))
with check (auth.uid() = sender_id or public.is_admin(auth.uid()));

-- NOTIFICATIONS
drop policy if exists "notifications_read_targeted" on public.notifications;
create policy "notifications_read_targeted"
on public.notifications
for select
using (
  target_user_id is null
  or target_user_id = auth.uid()
  or public.is_admin(auth.uid())
  or (
    target_role is not null
    and exists (
      select 1
      from public.profiles p
      where p.id = auth.uid()
        and p.role = public.notifications.target_role
        and p.status = 'approved'
    )
  )
);

drop policy if exists "notifications_insert_admin" on public.notifications;
create policy "notifications_insert_admin"
on public.notifications
for insert
with check (public.is_admin(auth.uid()));

drop policy if exists "notifications_update_admin" on public.notifications;
create policy "notifications_update_admin"
on public.notifications
for update
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

-- CAMPUS LOCATIONS
drop policy if exists "campus_locations_read_authenticated" on public.campus_locations;
create policy "campus_locations_read_authenticated"
on public.campus_locations
for select
using (auth.uid() is not null);

drop policy if exists "campus_locations_modify_admin" on public.campus_locations;
create policy "campus_locations_modify_admin"
on public.campus_locations
for all
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

-- STORAGE (avatars)
insert into storage.buckets (id, name, public)
values ('avatars', 'avatars', true)
on conflict (id) do nothing;

drop policy if exists "avatars_read_public" on storage.objects;
create policy "avatars_read_public"
on storage.objects
for select
using (bucket_id = 'avatars');

drop policy if exists "avatars_upload_owner" on storage.objects;
create policy "avatars_upload_owner"
on storage.objects
for insert
with check (
  bucket_id = 'avatars'
  and auth.uid()::text = split_part(name, '/', 1)
);

drop policy if exists "avatars_update_owner" on storage.objects;
create policy "avatars_update_owner"
on storage.objects
for update
using (
  bucket_id = 'avatars'
  and auth.uid()::text = split_part(name, '/', 1)
)
with check (
  bucket_id = 'avatars'
  and auth.uid()::text = split_part(name, '/', 1)
);

drop policy if exists "avatars_delete_owner_or_admin" on storage.objects;
create policy "avatars_delete_owner_or_admin"
on storage.objects
for delete
using (
  bucket_id = 'avatars'
  and (
    auth.uid()::text = split_part(name, '/', 1)
    or public.is_admin(auth.uid())
  )
);

